#!/usr/bin/env bash
set -Eeuo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "$ROOT"

BLUE='\033[1;34m'
GREEN='\033[1;32m'
YELLOW='\033[1;33m'
RED='\033[1;31m'
RESET='\033[0m'
BACKUP_FILE=''
TMP_DIR=''

log() {
    printf "%b[LaporKota Upgrade]%b %s\n" "$BLUE" "$RESET" "$1"
}

warn() {
    printf "%b[PERINGATAN]%b %s\n" "$YELLOW" "$RESET" "$1" >&2
}

fail() {
    printf "%b[GAGAL]%b %s\n" "$RED" "$RESET" "$1" >&2
    exit 1
}

cleanup() {
    if [[ -n "${TMP_DIR:-}" && -d "$TMP_DIR" ]]; then
        rm -rf "$TMP_DIR"
    fi
}

on_error() {
    local exit_code=$?
    printf "%b[UPGRADE TERHENTI]%b Terjadi kegagalan pada baris %s.\n" "$RED" "$RESET" "$1" >&2
    if [[ -n "${BACKUP_FILE:-}" ]]; then
        printf "Backup source sebelum perubahan tersedia di: %s\n" "$BACKUP_FILE" >&2
    fi
    exit "$exit_code"
}

trap cleanup EXIT
trap 'on_error $LINENO' ERR

[[ -f artisan && -f composer.json ]] || fail "Jalankan skrip ini dari root proyek Laravel yang berisi artisan dan composer.json."
command -v php >/dev/null 2>&1 || fail "PHP tidak ditemukan. Minimal PHP 8.3."
command -v composer >/dev/null 2>&1 || fail "Composer tidak ditemukan. Pasang Composer 2.x terlebih dahulu."
command -v node >/dev/null 2>&1 || fail "Node.js tidak ditemukan. Minimal Node.js 20."
command -v npm >/dev/null 2>&1 || fail "npm tidak ditemukan."
command -v tar >/dev/null 2>&1 || fail "Perintah tar tidak ditemukan."

php -r 'exit(version_compare(PHP_VERSION, "8.3.0", ">=") ? 0 : 1);' \
    || fail "PHP $(php -r 'echo PHP_VERSION;') tidak didukung. Minimal PHP 8.3."

NODE_MAJOR="$(node -p 'process.versions.node.split(".")[0]')"
[[ "$NODE_MAJOR" =~ ^[0-9]+$ ]] || fail "Versi Node.js tidak dapat dibaca."
(( NODE_MAJOR >= 20 )) || fail "Node.js $(node -v) tidak didukung. Minimal Node.js 20."

for extension in exif fileinfo gd mbstring pdo_mysql; do
    php -m | grep -qi "^${extension}$" || fail "Ekstensi PHP ${extension} belum aktif."
done

if [[ ! -f .env ]]; then
    cp .env.example .env
    warn "File .env belum ada. File dibuat dari .env.example. Periksa konfigurasi database dan API key sebelum production."
fi

TIMESTAMP="$(date +%Y%m%d_%H%M%S)"
BACKUP_DIR="$ROOT/storage/app/upgrade-backups"
mkdir -p "$BACKUP_DIR"
BACKUP_FILE="$BACKUP_DIR/source_before_gis_media_${TIMESTAMP}.tar.gz"

MANAGED_PATHS=(
    .env.example .gitignore LICENSE README.md app artisan bootstrap composer.json config database
    install.sh package.json phpunit.xml public resources routes tailwind.config.js tests vite.config.js
)
EXISTING_PATHS=()
for path in "${MANAGED_PATHS[@]}"; do
    [[ -e "$path" ]] && EXISTING_PATHS+=("$path")
done

if (( ${#EXISTING_PATHS[@]} > 0 )); then
    log "Membuat backup source lama"
    tar --exclude='public/build' --exclude='public/storage' -czf "$BACKUP_FILE" "${EXISTING_PATHS[@]}"
fi

TMP_DIR="$(mktemp -d)"
PAYLOAD_FILE="$TMP_DIR/upgrade_payload.tar.gz"
PAYLOAD_LINE="$(awk '/^__LAPORKOTA_PAYLOAD_BELOW__$/ { print NR + 1; exit }' "$0")"
[[ -n "$PAYLOAD_LINE" ]] || fail "Payload upgrade tidak ditemukan di dalam skrip."

tail -n "+${PAYLOAD_LINE}" "$0" | php -r '
    $encoded = stream_get_contents(STDIN);
    $decoded = base64_decode($encoded, true);
    if ($decoded === false) {
        fwrite(STDERR, "Payload Base64 tidak valid.\n");
        exit(1);
    }
    file_put_contents($argv[1], $decoded);
' "$PAYLOAD_FILE"

EXPECTED_SHA256="99c389622eae947f1fb0e59dd502fcdbe4dc884ebcf3c6fd29950a86ab03583b"
ACTUAL_SHA256="$(php -r 'echo hash_file("sha256", $argv[1]);' "$PAYLOAD_FILE")"
[[ "$ACTUAL_SHA256" == "$EXPECTED_SHA256" ]] || fail "Checksum payload tidak cocok. Unduh ulang skrip perbaikan."

log "Menerapkan source perbaikan dashboard, galeri, media, GPS, kategori, dan GIS"
tar -xzf "$PAYLOAD_FILE" -C "$ROOT"

mkdir -p \
    storage/app/public \
    storage/framework/cache/data \
    storage/framework/sessions \
    storage/framework/testing \
    storage/framework/views \
    storage/logs \
    bootstrap/cache
chmod -R ug+rwX storage bootstrap/cache

log "Memasang dan menyelaraskan dependensi PHP"
composer install --no-interaction --prefer-dist --optimize-autoloader
composer dump-autoload --optimize --no-interaction

log "Membersihkan cache sebelum migration"
php artisan optimize:clear

log "Menjalankan migration peningkatan database"
php artisan migrate --force

log "Mengonversi kategori dan media lama secara aman"
php artisan db:seed --class='Database\Seeders\UpgradeFeatureSeeder' --force

log "Menyiapkan symbolic link media publik"
php artisan storage:link >/dev/null 2>&1 || true

log "Memasang dependensi frontend"
npm install --no-audit --no-fund

log "Membangun aset production"
npm run build

log "Membangun ulang cache aplikasi"
php artisan optimize:clear
php artisan config:cache
php artisan route:cache
php artisan view:cache

cat > storage/app/laporkota-upgrade.json <<JSON
{
  "version": "2026.07.14-gis-media-dashboard-v2",
  "installed_at": "$(date -Iseconds)",
  "backup": "${BACKUP_FILE}"
}
JSON

printf "\n%bPerbaikan selesai.%b\n" "$GREEN" "$RESET"
printf "Backup source lama: %s\n" "$BACKUP_FILE"
printf "Fitur aktif: dashboard sidebar seragam, filter wilayah, galeri, multi-media, edit laporan belum disetujui, kategori dinamis, GPS dan alamat tersinkron, serta peta wilayah berbasis gelembung dan marker cluster.\n"
printf "Jalankan aplikasi dengan: php artisan serve\n"
exit 0

