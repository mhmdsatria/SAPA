cleanup() {
    rm -rf "$WORK_DIR"
}

restore_backup() {
    if [[ "$APPLIED" -ne 1 ]]; then
        return
    fi

    echo "Terjadi kegagalan. Mengembalikan file ke kondisi sebelum patch..."

    for file in "${FILES[@]}"; do
        rm -f "$ROOT/$file"
        if [[ -f "$BACKUP_ROOT/files/$file" ]]; then
            mkdir -p "$ROOT/$(dirname "$file")"
            cp -a "$BACKUP_ROOT/files/$file" "$ROOT/$file"
        fi
    done

    if [[ -f "$ROOT/artisan" ]]; then
        php "$ROOT/artisan" optimize:clear >/dev/null 2>&1 || true
    fi

    echo "Rollback selesai. Backup tetap tersedia di: $BACKUP_ROOT"
}

on_error() {
    local exit_code=$?
    restore_backup
    cleanup
    echo "Patch gagal pada baris ${BASH_LINENO[0]} dengan kode $exit_code." >&2
    exit "$exit_code"
}

